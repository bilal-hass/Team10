    private void ConfirmButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConfirmButton2MouseClicked
        if (NameText.getText().isBlank()) {
            ErrLabel.setText("Name must not be blank");
            return;
        }

        if (DescriptionText.getText().isBlank()) {
            ErrLabel.setText("Description must not be blank");
            return;
        }

        Discount d = DiscountController.createVariableDiscountPlan(NameText.getText(), DescriptionText.getText(), discountArray);
        ((DiscountTableModel)Parent.getDiscountTable().getModel()).addDiscount(d);

        this.dispose();
    }//GEN-LAST:event_ConfirmButton2MouseClicked

