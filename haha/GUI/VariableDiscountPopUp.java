        AddButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Float amount = Float.parseFloat(PercentageText.getText());
                Integer index = TaskTypeDropDown.getSelectedIndex();

                if (amount > .5 & ErrLabel.getText().isBlank()) {
                    ErrLabel.setText("Current amount is " + ((Float)(amount*100f)).toString() + "% Are you sure?" );
                    return;
                }

                if ( ((TaskDiscountTableModel)TaskDiscountsTable.getModel()).hasElementOfTaskType(index) ) {
                    ErrLabel.setText("TaskType already discounted");
                    return;
                }

                ErrLabel.setText("");

                TaskPercentPair data = new TaskPercentPair(index+1, amount);
                System.out.println(index+1);
                discountArray.add(data);

                ((TaskDiscountTableModel)TaskDiscountsTable.getModel()).addElement(index, amount);
            }
        });

        pack();
